package fresher;

public class Result {
	int id;
	float javaPoint;
	float phpPoint;
	float generalPoint;
	
	public Result() {}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public float getJavaPoint() {
		return javaPoint;
	}

	public void setJavaPoint(float javaPoint) {
		this.javaPoint = javaPoint;
	}

	public float getPhpPoint() {
		return phpPoint;
	}

	public void setPhpPoint(float phpPoint) {
		this.phpPoint = phpPoint;
	}

	public float getGeneralPoint() {
		return generalPoint;
	}

	public void setGeneralPoint(float generalPoint) {
		this.generalPoint = generalPoint;
	}


}
